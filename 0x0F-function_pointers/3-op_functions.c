#include "calc.h"

/**
 * op_add - Function
 * @a: Num1
 * @b: Num2
 *
 * Description: Returns sum of a and b.
 * Return: a + b, sum.
 * On error, stderr.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - Function
  * @a: Num1
  * @b: Num2
  *
  * Description: Returns sum of a and b.
  * Return: a - b, difference.
  * On error, stderr.
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - Function
  * @a: Num1
  * @b: Num2
  *
  * Description: Returns sum of a and b.
  * Return: a * b, product.
  * On error, stderr.
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function
 * @a: Num1
 * @b: Num2
 *
 * Description: Returns sum of a and b.
 * Return: a / b, quotient.
 * On error, stderr.
 */

int op_div(int a, int b)
{
         return (a / b);
}

/**
 * op_mod - Function
 * @a: Num1
 * @b: Num2
 *
 * Description: Returns sum of a and b.
 * Return: a % b, modulus.
 * On error, stderr.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
