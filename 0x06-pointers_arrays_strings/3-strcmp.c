#include "main.h"

/**
 * _strcmp - Function prototype
 * @s1: String comparor
 * @s2: String comparee
 *
 * Description: Compares 2 strings and returns 1 if s1
 * is comtained in s2, 0 otherwise.
 * Return: 1 if contained, 0  otherwise.
 * On error, stderr.
 */

int _strcmp(char *s1, char *s2)
{
	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}

	return (*s1 - *s2);
}
